import math
# Math functions = Integrierte Funktionen, die auf numerische Werte angewendet werden können, um mathematische Operationen durchzuführen


pi = 3.14

x = 1
y = 2
z = 3

# Rundet zum nächsten INT auf/ab
print (round(pi))

# Rundet auf
print(math.ceil(pi))

# Rundet ab
print(math.floor(pi))

# Gibt absoluten Wert der angegebenen Zahl (Bei -3.14 zeigt an, wie viel zur 0 fehlt)
print(abs(pi))

# Gibt den Wert von x hoch y (xy) 
print(pow(pi, 2))

# Gibt Quadratwurzel an
print(math.sqrt(pi))

# Findet größten Wert
print(max(x,y,z))

# Findet kleinsten Wert
print(min(x,y,z))